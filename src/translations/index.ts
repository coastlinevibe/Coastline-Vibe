// Helper function to get a translation by key path
export function getTranslation(lang: string, keyPath: string, fallback?: string): string {
  const keys = keyPath.split('.');
  const langObj = translations[lang as keyof typeof translations] || enTranslations;
  
  let value: any = langObj;
  for (const key of keys) {
    if (!value || typeof value !== 'object') {
      return fallback || keyPath;
    }
    value = value[key];
  }
  
  return typeof value === 'string' ? value : fallback || keyPath;
}

